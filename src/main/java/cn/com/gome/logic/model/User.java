package cn.com.gome.logic.model;

import java.io.Serializable;

/**
 * 用户对象
 */
public class User implements Serializable {
	private static final long serialVersionUID = 1L;

	private String appId;
	private long uid;
	private int clientId;
	private int clientType;
	private String token;
	private long tokenExpires;
	private String pcToken;
	private long pcTokenExpires;
	private String webToken;
	private long webTokenExpires;
	private String apnsToken;
	private String deviceId;
	private long updateTime;
	private long createTime;

	public String getAppId() {
		return appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public int getClientId() {
		return clientId;
	}

	public void setClientId(int clientId) {
		this.clientId = clientId;
	}

	public int getClientType() {
		return clientType;
	}

	public void setClientType(int clientType) {
		this.clientType = clientType;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public long getTokenExpires() {
		return tokenExpires;
	}

	public void setTokenExpires(long tokenExpires) {
		this.tokenExpires = tokenExpires;
	}

	public String getPcToken() {
		return pcToken;
	}

	public void setPcToken(String pcToken) {
		this.pcToken = pcToken;
	}

	public String getApnsToken() {
		return apnsToken;
	}

	public void setApnsToken(String apnsToken) {
		this.apnsToken = apnsToken;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public long getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(long updateTime) {
		this.updateTime = updateTime;
	}

	public long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}

	public long getPcTokenExpires() {
		return pcTokenExpires;
	}

	public void setPcTokenExpires(long pcTokenExpires) {
		this.pcTokenExpires = pcTokenExpires;
	}

	public String getWebToken() {
		return webToken;
	}

	public void setWebToken(String webToken) {
		this.webToken = webToken;
	}

	public long getWebTokenExpires() {
		return webTokenExpires;
	}

	public void setWebTokenExpires(long webTokenExpires) {
		this.webTokenExpires = webTokenExpires;
	}
}
